package com.edutilos.main2


object SimpleMath {
   def add(x:Double  , y:Double): Double = {
      return x +y
   }

    def sub(x:Double , y:Double): Double = {
      return x -y
    }

    def mult(x:Double, y:Double):Double = {
       return x * y
    }

    def div(x:Double, y:Double):Double = {
      return x / y
    }
}
